let jour = 3;
let nomDuJour;

switch (jour) {
  case 1:
    nomDuJour = "Lundi";
    break;
  case 2:
    nomDuJour = "Mardi";
    break;
  case 3:
    nomDuJour = "Mercredi";
    break;
  case 4:
    nomDuJour = "Jeudi";
    break;
  case 5:
    nomDuJour = "Vendredi";
    break;
  case 6:
    nomDuJour = "Samedi";
    break;
  case 7:
    nomDuJour = "Dimanche";
    break;
  default:
    nomDuJour = "Jour invalide";
}

console.log(nomDuJour);
